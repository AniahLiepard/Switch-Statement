public class ProjectNine {
	public static void main(String args[]){
		int dog = 3;
		
		switch (dog){
		case 1:
			System.out.println("You have a Bloodhound.");
			break;
		case 2:
			System.out.println("You have a Doberman.");
			break;
		case 3:
			System.out.println("You have a Great Dane.");
			break;
		default:
			System.out.println("You have a Rottweiler. How unfortunate.");
			break;
		}
	}
}
